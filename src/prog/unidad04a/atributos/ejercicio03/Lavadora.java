package prog.unidad04a.atributos.ejercicio03;

/**
 * Representamos una lavadora
 */

public class Lavadora {
  
  /**
   * Establecemos el precio base de la lavadora. Tiene que ser un double mayor que 0.
   */
  
  public double precioBase;
  
  /**
   * Establecemos el color de la lavadora. Tiene que ser blanco, negro, rojo azul o gris.
   */
  
  public String color;
  
  /**
   * Establecemos la calificación energética, tiene que estar comprendida entre la A y la F cinluidas.
   *  
   */
  
  public char calificacionEnergetica;
  
  /**
   * Establecemos el peso de la lavadora, tiene que ser un número decimal positivo.
   */
  
  public double peso;
  
  /**
   * Establecemos la carga máxima de la lavadora, tiene que ser un número decimal positivo.
   */
  
  public double cargaMaxima;
  
  /**
   * Número total de lavadoras creadas-
   */
  
  public static int lavadorasCreadas;
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  

}
