package prog.unidad04a.atributos.ejercicio02;

/**
 * Representa a una persona
 */

public class Persona {

  /**
   *  Nombre de la persona
   */
  
  public String nombre;
  
  /**
   * Edad de la persona. Debe de ser un número entero y estar comprendida entre 0 y 100.
   */
  
  public int edad;
  
  /**
   * DNI de la persona. Es una cadena de texto formada por 8 dígitos y una letra.
   */
  
  public String dni;
  
  /**
   * Sexo de la persona, debe de ser H o M.
   */
  
  public char sexo;
  
  /**
   * El peso de la persona. Puede ser un número decimal y mayor que cero.
   */
  
  public double peso;
  
  
  /**
   * Altura de la persona. Es un double, tiene que ser mayor que cero.
   */
  
  public double altura;
  
  
}
























